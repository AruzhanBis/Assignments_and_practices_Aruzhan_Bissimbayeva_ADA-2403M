/*
Задание 1. Реализация сортировок без параллелизма.

В данной программе реализованы три классических алгоритма сортировки:
сортировка пузырьком, сортировка выбором и сортировка вставками.
Все алгоритмы реализованы без использования параллельного программирования
и выполняются последовательно.

Программа создаёт массив случайных целых чисел, выводит его на экран,
а затем сортирует копии массива каждым из трёх алгоритмов, выводя
результаты сортировки для наглядного сравнения работы алгоритмов.
*/

#include <iostream> // Подключение библиотеки для ввода и вывода (cout, endl)
#include <vector>   // Подключение контейнера vector для хранения массива
#include <cstdlib>  // Подключение библиотеки для функций rand() и srand()
#include <ctime>    // Подключение библиотеки для получения текущего времени (time)

using namespace std;

// Функция для вывода массива
void printArray(const vector<int>& arr) {   // Проход по всем элементам массива
    for (int num : arr) {
        cout << num << " ";                 // Вывод текущего элемента
    }
    cout << endl;                           // Переход на новую строку
}

// Сортировка пузырьком
void bubbleSort(vector<int>& arr) {         
    int n = arr.size();                     // размер массива
    for (int i = 0; i < n - 1; ++i) {       // внешний цикл по проходам
        for (int j = 0; j < n - i - 1; ++j) {      // внутренний цикл сравнения
            if (arr[j] > arr[j + 1]) {             // сравнение соседних элементов
                swap(arr[j], arr[j + 1]);          // обмен элементов
            }
        }
    }
}

// Сортировка выбором
void selectionSort(vector<int>& arr) {      
    int n = arr.size();                     // размер массива
    for (int i = 0; i < n - 1; ++i) {       // внешний цикл по позиции
        int minIndex = i;                   // индекс минимального элемента
        for (int j = i + 1; j < n; ++j) {   // поиск минимума в оставшейся части
            if (arr[j] < arr[minIndex]) {   // если найден меньший элемент
                minIndex = j;               // обновление индекса минимума
            }
        }
        swap(arr[i], arr[minIndex]);        // обмен текущего и минимального
    }
}

// Сортировка вставками
void insertionSort(vector<int>& arr) {
    int n = arr.size();                     // размер массива
    for (int i = 1; i < n; ++i) {           // начинаем со второго элемента
        int key = arr[i];                   // сохраняем текущий элемент
        int j = i - 1;                      // индекс предыдущего элемента
        while (j >= 0 && arr[j] > key) {    // пока элементы больше key
            arr[j + 1] = arr[j];            // сдвигаем элемент вправо
            --j;                            // смещение индекса
        }
        arr[j + 1] = key;                   // вставка key в нужную позицию
    }
}

int main() {
    srand(time(0));    // Инициализация генератора случайных чисел текущим временем

    // Создание массива случайных чисел
    int size = 10;              // Размер массива
    vector<int> arr(size);      // Создание вектора заданного размера
    for (int i = 0; i < size; ++i) {
        arr[i] = rand() % 100; // случайные числа от 0 до 99
    }

    cout << "Исходный массив:\n";    // Вывод текста 
    printArray(arr);                 // Вывод исходного массива

    vector<int> arrBubble = arr;                      // Копия массива для сортировки пузырьком
    bubbleSort(arrBubble);                            // Сортировка пузырьком
    cout << "\nМассив после сортировки пузырьком:\n"; // Вывод текста
    printArray(arrBubble);                            // Вывод результата сортировки пузырьком

    vector<int> arrSelection = arr;                  // Копия массива для сортировки выбором
    selectionSort(arrSelection);                      // Сортировка выбором
    cout << "\nМассив после сортировки выбором:\n";  // Вывод текста
    printArray(arrSelection);                        // Вывод результата сортировки выбором

    vector<int> arrInsertion = arr;                     // Копия массива для сортировки вставками
    insertionSort(arrInsertion);                        // Сортировка вставками
    cout << "\nМассив после сортировки вставками:\n";   // Вывод текста
    printArray(arrInsertion);                           // Вывод результата сортировки вставками

    return 0;                       // Завершение программы
}       
